package com.promotionsapi.pojo.response;

public class Images {

	private String id;
	private String url;
	private int width;
	private int height;

	public String getId() {
		return id;
	}

	public String getUrl() {
		return url;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
